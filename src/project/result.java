    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        String result="";
        String s1 =id.getText();
        String s2 =sitting_no.getText();
        if (s1.isEmpty()||s2.isEmpty())
        {JOptionPane.showMessageDialog(this,"fill all fields");}
        else if(isNumber(s1)){JOptionPane.showMessageDialog(this,"Please enter the right ID");}
     else if(isNumber(s2)){JOptionPane.showMessageDialog(this,"Please enter the right SittingNumber");}
     else{ 
      try
      {
       Class.forName(driver);
       Connection con = DriverManager.getConnection(url,user,password);
       String sql = "select collage from applicants where id='"+s1+"' ";
       st = con.createStatement();
       rs= st.executeQuery(sql);
       String collage = "";
      while(rs.next()){
       collage = rs.getString("collage");}
      // System.out.print(collage);
      if(collage.isEmpty()){ JOptionPane.showMessageDialog(this,"Please Reapply and choose different collages");}
      else{
       JOptionPane.showMessageDialog(this,"Congrats!\n Your collage is "+collage);}
      }
      catch(Exception e){ JOptionPane.showMessageDialog(this,e.getMessage());}

     }
    }//GEN-LAST:event_searchActionPerformed

